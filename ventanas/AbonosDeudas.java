    public void llenarTablaAbonoPartidas(String idDeuda) {

        modelo = (DefaultTableModel) jTable_abonos.getModel();

        String consulta = "select fechaGasto, id, idPrespuesto, observaciones, valor from gastospresupuestos \n"
                + "where idPartida=?";

        try {
            Connection cn = Conexion.Conectar();
            PreparedStatement pst = cn.prepareStatement(consulta);
            pst.setString(1, idDeuda);

            ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                
                Object[] nuevo = new Object[5];
                nuevo[0] = rs.getString("fechaGasto");
                nuevo[1] = rs.getString("id");
                nuevo[2] = rs.getString("idPrespuesto");
                nuevo[3] = rs.getString("observaciones");
                nuevo[4] = ConvertirIntAMoneda(Double.parseDouble(rs.getString("valor")));

                modelo.addRow(nuevo);
            }

            jTable_abonos.setModel(modelo);

            TableRowSorter<TableModel> ordenador = new TableRowSorter<TableModel>(modelo);
            jTable_abonos.setRowSorter(ordenador);

            cn.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error en leer los abonos de las partidas");
            e.printStackTrace();
        }

    }
    
